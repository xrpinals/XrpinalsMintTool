type Signature []byte

func (a Signature) MarshalText() ([]byte, error) {
	t := hex.EncodeToString(a[:])
	return []byte(t), nil
}

func (a *Signature) UnmarshalText(input []byte) error {
	b, err := hex.DecodeString(string(input))
	if err != nil {
		return err
	}
	*a = append(*a, b...)
	return nil
}

type UTCTime uint32

func (a UTCTime) MarshalText() ([]byte, error) {
	return []byte(property.UTCToRFC3339(uint64(a))), nil
}

func (a *UTCTime) UnmarshalText(input []byte) error {
	t, err := property.RFC3339ToUTC(string(input))
	if err != nil {
		return err
	}
	*a = UTCTime(t)
	return nil
}
